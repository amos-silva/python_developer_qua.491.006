# Operadores Aritimeticos
# variaveis
x = 5
y = 2

# operações
soma = x+y
sub = x-y
mult = x*y
div = x/y
resto = x%y
potencia = x**y

# Saida de dados - CONTATENAÇÃO COM FUNÇÃO .format
print("A soma de {} e {} é: {} ".format(x, y, soma))

# Saida de dados - CONTATENAÇÃO COM fstring
print(f"A Subtração de {x} e {y} é: {sub}")
print(f"A Multiplicação de {x} e {y} é: {mult}")
print(f"A Divisão de {x} e {y} é: {div}")
print(f"A RESTO de {x} e {y} é: {resto}")
print(f"A POTENCIA de {x} e {y} é: {potencia}")